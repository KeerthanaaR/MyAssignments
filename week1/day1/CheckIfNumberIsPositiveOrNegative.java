package week1.day1;

public class CheckIfNumberIsPositiveOrNegative {

public static void main(String[] args) {
	int number = -0;
	
	if(number>0)
	{
		System.out.println("The number is positive");
	}
		
	else if(number<0)
	{
		System.out.println("The number is Negative");
	}
	else
	{
		System.out.println("The number is 0");
	}
}
}
